#ForLoops

#We take a look at for loops and we use the turtle module

#Turtle draws lines on the screen and helps us learn and practice in a entertaining way.
import turtle
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.color('green')
turtle.forward(50)
turtle.color('red')

for steps in range(4):
    turtle.forward(100)
    turtle.right(90)
